<script setup>
import {reactive} from 'vue'
const state = reactive({
  form: {
    id:'',
    pw:''
  }
});
// 아이디. 패스워드에 문자가 하나도 안 적혀있으면 alert('아이디, 패스워드를 작성해주세요.')
// 적혀있으면 alert('~~님 환영합니다!')
const login = () => 
    // 예외처리
    { if (state.form.id && state.form.pw) {
      alert(`${state.form.id}님 환영합니다!`)
      }
      else {alert('아이디, 패스워드를 작성해주세요.')}
    } ;
</script>
<template>
<h3>page125.vue</h3>
<!-- prevent 막는다. 원래 submit에서 하는 작업을 막는다. -->
<form @submit.prevent="login">
  <input type="text" placeholder="아이디" v-model="state.form.id">
  <input type="password" placeholder="패스워드" v-model="state.form.pw">
  <button>로그인</button>
</form>
</template>

